package com.crypto.jmes.bean;

public class VolumeAnalysis {
	
	private double avgVolume;
	private double lastVolume;
	private double highestVolume;
	
	public double getAvgVolume() {
		return avgVolume;
	}
	public void setAvgVolume(double avgVolume) {
		this.avgVolume = avgVolume;
	}
	public double getLastVolume() {
		return lastVolume;
	}
	public void setLastVolume(double lastVolume) {
		this.lastVolume = lastVolume;
	}
	public double getHighestVolume() {
		return highestVolume;
	}
	public void setHighestVolume(double highestVolume) {
		this.highestVolume = highestVolume;
	}
	
	

}
